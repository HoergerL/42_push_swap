#include "libft.h"

int	ft_check_nbr(char *nbr)
{
	int	i;

	i = 0;
	while (nbr[i] != '\0')
	{
		if (ft_strchr("+-0123456789", nbr[i]) == NULL)
			return (0);
		i++;
	}
	return (1);
}
